# 함수 배운 내용 점검하기.
# 1. 더하기 -> plus
def plus(a, b):
    result = a + b
    return result
# 2. 빼기 minus
def minus(a, b):
    return a - b
# 3. 곱하기 multi
def multi(a, b):
    return a * b
# 4. 나누기 divide
def divide(a, b):
    return a / b


# In[4]:


print(plus(1, 2))
print(minus(3, 0))
print(multi(2, 3))
print(divide(4, 3))


